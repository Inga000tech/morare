const groundingWords = ["Balance", "Presence", "Grounding", "Peace", "Observation", "Stillness", "Breath", "Connection"];

export function getIntegrationText(dateString) {
  const date = new Date(dateString);
  const dayOfYear = Math.floor((date - new Date(date.getFullYear(), 0, 0)) / 86400000);
  
  return {
    word: groundingWords[dayOfYear % groundingWords.length],
    title: "Integration Period",
    meaning: "No major celestial shift today. The work is internal. The transformation is subtle. Integration is where growth becomes permanent."
  };
}
